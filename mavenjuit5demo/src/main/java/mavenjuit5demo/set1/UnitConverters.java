package mavenjuit5demo.set1;

public class UnitConverters {
	public static Integer convertMeterToCentimenter(int meter) {
		return meter*100;
	}
	public static double convertMeterToFeet(int meter) {
		return meter*3.3;
	}
	public static int convertMeterTokilometer(int meter) {
		return meter/1000;
	}
}
